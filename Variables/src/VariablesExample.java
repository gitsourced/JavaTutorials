public class VariablesExample {

	public static void main(String[] args) {
		
		// Integer Example
		int firstInt = 14;
		System.out.println(firstInt);

		// Float Example
		int firstFloat = 14;
		System.out.println(firstFloat / 3);

		// Double Example
		int firstDouble = 14;
		System.out.println(firstDouble / 3);

		// Boolean Example
		boolean check = true;
		System.out.println(check);
		System.out.println(!check);

		// Char Example
		char firstChar = '&';
		System.out.println(firstChar);
		int charValue = firstChar;
		System.out.println("The intgeger value for " + firstChar + " is " + charValue);
	}
}
