public class VariablesPractice {

	public static void main(String[] args) {

			//Integer Example
			int firstInt = 14;
			System.out.println(firstInt);
			
			//Float Example
			float firstFloat = 14;
			System.out.println(firstFloat / 2);
			
			//Double Example
			double firstDouble = 14;
			System.out.println(firstDouble / 3);
			
			//Boolean Example
			boolean check = true;
			System.out.println(check);
			System.out.println(!check);
			
			//Char Example
			char firstChar = '&';
			int charValue = firstChar;
			System.out.println(firstChar);
			System.out.println("The integer value for " + firstChar + " is " + charValue);
		}
}
